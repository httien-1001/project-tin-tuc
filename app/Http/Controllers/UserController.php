    public function update(Request $request, User $user)
    {
        $user->update($request->only('name','email'));
        if(is_array($request->role)){
            foreach($request->role as $role_id){
                UserRole::create([
                    'user_id'=>$user->id,
                    'role_id'=>$role_id,]);

            }
        }
        return redirect()->route('admin.user.index');
    }

